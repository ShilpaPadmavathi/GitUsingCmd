package com.ntt;

public class MaxVariables {
	public static void main(String[] args) {
		int intValue=Integer.MAX_VALUE;
		float floatValue=Float.MAX_VALUE;
		byte byteValue=Byte.MAX_VALUE;
		long longValue=Long.MAX_VALUE;
		double doubleValue=Double.MAX_VALUE;
		boolean booleanValue=Boolean.FALSE;
		
		System.out.println("Largest Integer Value "+intValue);
		System.out.println("Largest Float Value "+floatValue);
		System.out.println("Largest Byte Value "+byteValue);
		System.out.println("Largest Long Value "+longValue);
		System.out.println("Largest Double Value "+doubleValue);
		System.out.println("Boolean Value "+booleanValue);
	}

}
